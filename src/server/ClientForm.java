    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String server = jTextField1.getText();
            int porta = Integer.parseInt(jTextField2.getText());
            String name = jTextField3.getText();

            socket = new Socket(server, porta);

            saida = new ObjectOutputStream(socket.getOutputStream());
            entrada = new ObjectInputStream(socket.getInputStream());

            Message loginMessage = new Message(name, this.color, MessageType.LOGIN_REQUEST);
            saida.writeObject(loginMessage);
            saida.reset();

            Message newMessage = (Message) entrada.readObject();
            if (newMessage.getType().equals(MessageType.CHANGED)) {
                receptor = new Receptor();
                receptor.start();
                this.balls = (List<Ball>) newMessage.getContent();
                paintComponent();
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, newMessage.getContent(), "Erro", JOptionPane.ERROR_MESSAGE);
            }

            this.requestFocus();
        } catch (HeadlessException | IOException | ClassNotFoundException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

