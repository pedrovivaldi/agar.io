    private void paintComponent() {
        g.setColor(Color.white);
        g.fillRect(0, 0, 1000, 1000);
        for (Ball ball : balls) {
            g.setColor(ball.getColor());
            g.drawString(ball.getName(), ball.getX() - 10, ball.getY() - 10);
            g.fillOval(ball.getX(), ball.getY(), ball.getRadius(), ball.getRadius());
        }
    }

    private class Receptor extends Thread {

        @Override
        public void run() {
            Message newMessage;
            for (;;) {
                try {
                    if (socket.isConnected() && !socket.isClosed() && !this.isInterrupted()) {
                        newMessage = (Message) entrada.readObject();
                        if (newMessage != null && newMessage.getType() != null) {
                            if (newMessage.getType().equals(MessageType.CHANGED)) {
                                balls = (List<Ball>) newMessage.getContent();
                                paintComponent();
                            } else if (newMessage.getType().equals(MessageType.LOSE)) {
                                balls.clear();
                                paintComponent();
                               
                                this.interrupt();
                                socket.close();
                                
                                JOptionPane.showMessageDialog(null, "Você perdeu! Reconecte-se para continuar jogando!", "Fim de Jogo!", JOptionPane.ERROR_MESSAGE);
                                
                                jButton1.setEnabled(true);
                                jButton2.setEnabled(false);
                            }
                        }
                    }
                } catch (IOException | ClassNotFoundException ex) {
                    Logger.getLogger(Server.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

